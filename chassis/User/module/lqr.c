/**
 * @brief LQR K ľŘŐóĽĆËă
 *
 * @param x [theta dtheta x dx pitch dpitch]
 * @param u [t tp]
 * @param len leg length
 */
void LQR_Control(float *x, float *u, float len)
{
	int i, j, n;
	float k_[6][2];

	// ŃˇÔńĎµĘý
	int8_t coef_idx = (int)roundf(fabsf(x[0]) / (PI / 12)); // ŃˇÔńĎµĘý
	coef_idx = coef_idx < 0	  ? 0
			   : coef_idx > 4 ? 4
							  : coef_idx; // ĎŢ·ů
	lqr_coe = lqr_k_coef[coef_idx];

	// ĽĆËă k
	for (i = 0; i < 6; i++)
	{
		for (j = 0; j < 2; j++)
		{
			n = i * 2 + j;
			k_[i][j] = (*lqr_coe)[n][0] * len + (*lqr_coe)[n][1] * len * len + (*lqr_coe)[n][2] * len * len * len + (*lqr_coe)[n][3];
		}
	}

	// ×ŞÖĂ
	for (i = 0; i < 6; i++)
	{
		for (j = 0; j < 2; j++)
		{
			k[j][i] = k_[i][j];
		}
	}

	// ĽĆËă u = Kx
	u[0] = u[1] = 0;
	for (int i = 0; i < 6; i++)
	{
		u[0] += x[i] * k[0][i];
		u[1] += x[i] * k[1][i];
	}
}
